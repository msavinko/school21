int	ft_is_prime(int nb)
{
	int	i;
	int	k;

	i = 2;
	k = 0;
	while (nb >= i)
	{
		if (nb % i == 0)
			k++;
		i++;
	}
	if (k==1) 
		return (1);
	return (0);
}
